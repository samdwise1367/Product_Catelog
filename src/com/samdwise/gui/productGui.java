    private void jButtonInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonInsertActionPerformed
        // TODO add your handling code here:
        if(productAction.checkInput(jTextFieldName.getText(), jTextFieldPrice.getText(),dateChooserDateAdd.getText())){
            
            byte[] imageInput = null;
            
                float price = Float.parseFloat(jTextFieldPrice.getText());
            try {
                imageInput = productAction.imageByte(path);
                ProductTO productTo = new ProductTO(jTextFieldName.getText(),price,dateChooserDateAdd.getText(),imageInput);
                productAction.insertData(productTo);
                fillTable(); //update the table
                clearFields();//clear the fields
                System.out.println(productTo.getImage());
            } catch (IOException ex) {
                Logger.getLogger(productGui.class.getName()).log(Level.SEVERE, null, ex);
            }
                            
        }else{
            JOptionPane.showMessageDialog(null,"Some fidels are empty");
        }
    }//GEN-LAST:event_jButtonInsertActionPerformed

